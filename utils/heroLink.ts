export function getHeroLink(heroId: number) {
  return `https://game.gtimg.cn/images/yxzj/img201606/heroimg/${heroId}/${heroId}.jpg`
}

export function getHeroBigSkin(heroId: number, skinNumber: number) {
  return `https://game.gtimg.cn/images/yxzj/img201606/skin/hero-info/${heroId}/${heroId}-bigskin-${skinNumber}.jpg`
}
